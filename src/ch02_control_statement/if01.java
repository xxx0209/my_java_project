package ch02_control_statement;

public class if01 {
    public static void main(String[] args) {
        int su = 8;

        if(su%2 == 0){
            System.out.println(su + "는(은) 짝수입니다.");
        }

        if(su%2 == 0){
            System.out.println(su + "는(은) 짝수입니다.");
        }else{
            System.out.println(su + "는(은) 홀수입니다.");
        }

        System.out.println("크크크크크크");

    }
}
